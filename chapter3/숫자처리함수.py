# 절대값 abs();
print(abs(-5)) #5

# 제곱 pow();
print(pow(4, 2)) # 4^2 = 4*4 = 16;

# 최대값 max();
print(max(5, 12)) # 12;

# 최소값 min();
print(min(5, 12)) # 5;

# 반올림
print(round(3.14)) # 3;
print(round(4.99)) # 5;

from math import *;
print(floor(4.99)); # 내림, 4;
print(ceil(3.14)) # 올림, 4;
print(sqrt(16)) # 제곱근, 4;