# 간단한 수식
print(2 + 3 * 4)  # 14

# 괄호 사용
print ((2 + 3) * 4) # 20

# 변수에 할당
number = 2 + 3 * 4 # 14
print(number)
number = number + 2 # 16
print(number)

number += 2 # 18
print(number)

number *= 2 # 36
print(number)
number /= 2 # 18
print(number)
number -= 2 # 16
print(number)

# 나머지 계산
number %= 5 # 1
print(number)